// Function to display pagination for sexual wellness products
function displaySexualWellnessPagination() {
  const totalPages = Math.ceil(sexualWellnessProducts.length / productsPerPage);
  const paginationContainer = document.querySelector('.sexual-wellness-pagination-container');

  let paginationHTML = '';
  for (let i = 1; i <= totalPages; i++) {
      const isActive = currentSexualWellnessPage === i ? 'active' : '';
      paginationHTML += `<li class="page-item ${isActive}"><a class="page-link" href="#" onclick="changeSexualWellnessPage(${i})">${i}</a></li>`;
  }
  paginationContainer.innerHTML = paginationHTML;
}

